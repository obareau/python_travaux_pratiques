# Let's play with datatypes

print (type(23))            # int
print (type(3.14))          # float
print (type('hello world')) # str

print (type("23"))          # str
print (type("3.24"))        # str

print (type(None))          # NoneType
print (type(True))          # bool
print (type(False))         # bool

print (type([]))            # list
print (type(()))            # tuple
print (type({}))            # dict


